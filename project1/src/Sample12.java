
public class Sample12 {
	
	public static void main(String[] args) {
		
		//부정연산자 not(!)의 사용예 -> toggle
		//toggle is like a switch.
		boolean play = true;
		System.out.println(play);
		
		play = !play;
		System.out.println(play);
		
		play = !play;
		System.out.println(play);
	}//main

}//end class
