
public class Switch4 {
	
	//switch expressions#1
	public static void main(String[] args) {
		int number = 6;
		
		switch(number) {
		
		case 7 -> { //:이 없음
			System.out.println("7");
			System.out.println("7");
			System.out.println("7");
			System.out.println("7");
			System.out.println("7");
		} //case 7
		
		case 6 -> {
			System.out.println("6");
		}//case 6
		
		}//switch
	}//main

}//end class
